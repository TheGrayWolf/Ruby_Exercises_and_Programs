# Capitalize rewrites the string and Capitalizes the first letter and puts the rest of the string in uncapitalized

puts "hello".capitalize
puts "hello World".capitalize

puts
# Upcase / Downcase makes the entire string to uppcase or downcase
puts "hello".upcase
puts "HeLlO".downcase

puts
# Swapcase switches the casing of the letter
puts "hELLO".swapcase